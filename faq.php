<hr align="center">
<h1 align="center"><a name="A1">Login</a></h1>
<p align="center"><br />
The Login page is where you enter your username and password and log in to the game. You can also register and get your password back if you have forgotten it.</p>

<hr align="center">
<h1 align="center"><a name="A2">FAQ</a></h1>
<p align="center"><br />
The FAQ is the document that you are currently reading. FAQ itself stands for "Frequently Asked Questions". This is here to refer to if you are unsure about something in the game. If it is unable to help, you can ask an Administator or a Moderator.</p>

<hr align="center">
<h1 align="center"><a name="A3">Terms Of Service</a></h1>
<p align="center"><br />
The Terms Of Service are the set rules of the game. They are in effect for everyone. However, the Moderators do get a different set of rules, which normal players shouldn't worry about.</p>

<hr align="center">
<h1 align="center"><a name="A4">Helpdesk</a></h1>
<p align="center"><br />
The Help Desk is a system used for players to confidentially ask Administrators/Moderators questions about the game, report cheats or exploits, and recover tokens from a dead account.</p>
  
<hr align="center">
<h1 align="center"><a name="A5">Tokens</a></h1>
<p align="center"><br />
Tokens are bought from other players or purchased using a credit card or a bank account. The price to buy them using a credit card is stated before you buy the Credentials. Credentials are used to purchase an assortment of different goods or advantages to give you the edge that you need in the game. Paypal is needed to purchase Credentials with a credit card/bank account.</p>
  
<hr align="center">
<h1 align="center"><a name="A6">Stats</a></h1>
<p align="center"><br />
Learn about your current standings, who owns what property, or other general game information and numbers.</p>

<hr align="center">
<h1 align="center"><a name="A8">Profile</a></h1>
<p align="center"><br />
Your profile is where you can display information to other players without using the messaging system. It can be helpful for showing things like what you have accomplished or have succeeded in doing.</p>

<hr align="center">
<h1 align="center"><a name="A9">Notepad</a></h1>
<p align="center"><br />
Your notepad is a private document where you can save any text, and only you can see it.</p>

<hr align="center">
<h1 align="center"><a name="A10">Messages</a></h1>
<p align="center"><br />
The messaging system is one of the ways that you can use to contact other players. Your accomplishments will be awarded via messages to you, from us. As well as accomplishments, your Organised Robberies results are given to you via the messaging system.</p>

<hr align="center">
<h1 align="center"><a name="A11">Main Forums</a></font></h1>
<p align="center"><br />
The Main Forums are the general areas where all of the players can join and discuss topics within the game. Players can post their own topics, or reply to posts made by Administrators, Moderators, or other players. Creating a topic is simple. Select the "Create New Topic" button located at the top of the page, then simply fill in the title, the body, click Submit, and your topic will be posted for other players to see and talk in. To post replies in another persons' topic, or even your own, simply scroll to the bottom of the topic view screen, type your message in the box provided, and click the Reply button located just below your message.</p>

<hr align="center">
<h1 align="center"><a name="A12">Network Forums</a></h1>
<p align="center"><br />
Like the main forums, people can post discussions, but these forums are only for Network members. Everyone in your Network will be able to view them, but no one else can.</p>

<hr align="center">
<h1 align="center"><a name="A13">Online Players</a></h1>
<p align="center"><br />
The online players shows every player that is online at that time. Click on the players username, and you can view their profile.</p>
 
<hr align="center">
<h1 align="center"><a name="A14">Search</a></h1>
<p align="center"><br />
The same as online players, but this lets you search for any player in the game, be they online/dead/banned/offline, and enables you to view their profile.</p>

<hr align="center">
<h1 align="center"><a name="A15">Global Market</a></h1>
<p align="center"><br />
The Global Market is Global Takeover&#8217;s personal eBay. Using this system you can sell pretty much anything openly or anonymously, and other players can buy these off the page without you having to be online.</p>

<hr align="center">
<h1 align="center"><a name="A16">Black Market</a></h1>
<p align="center"><br />
The Black Market. If you need something they've got it. Down here they have guns, bullets, armour, and explosives. Almost anything you need will be here somewhere.<br><br>
Note: For Armor, each level counts as 25% Armor. </p>

<hr align="center">
<h1 align="center"><a name="A17">Crimes</a></h1>
<p align="center"><br />
Crimes are your basic way of getting money or weapons. Just click where you want to steal from, choose what you want to steal (Money or Weapons), and commit the crime, by pressing the Do It! button.</p>

<hr align="center">
<h1 align="center"><a name="A18">Grand Theft Auto</a></h1>
<p align="center"><br />
Robbing a vehicle is similar to Crimes, but instead you're stealing cars. They are rare to steal, but the cars are used in either organised robberies or they can be sold.</p>
  
<hr align="center">
<h1 align="center"<a name="A19">Hijack</a></h1>
<p align="center"><br>
Hijacking an aircraft is the same as robbing vehicles, except you steal planes not cars. Planes can be used to travel from place to place. </p>

<hr align="center">
<h1 align="center"><a name="A20">Pirate</a></h1>
<p align="center"><br>
Pirating a ship is the same as robbing vehicles or hijacking airplanes, but you steal a boat instead. Boats are used for the missions, so always keep one with you.</p>

<hr align="center">
<h1 align="center"><a name="A21">Inventory</a></h1>
<p align="center"><br>
Your personal inventory, it contains all the weapons you have stolen or bought over your travels. They can be sold but your weapons do not need to be repaired.  
  </p>

<hr align="center">
<h1 align="center"><a name="A22">Garage/Hanger/Dock</a></h1>
<p align="center"><br>
The Garage/Hanger/Dock is where your stolen vehicles, aircrafts and vessels are kept for you to sell, repair or just collect. </p>

<hr align="center">
<h1 align="center"><a name="A23">Heist</a></h1>
<p align="center"><br>
  Heists- if you are ever low on money or just need more, do a heist. This unlike the rest of the crimes cannot be done alone, it requires 4 players. Each player has a very important job in the robbery, and for it to be successful you have to get the right equipment together and have a bit of luck.<br>
The jobs go as follow:<br>
  <strong>The hacker</strong>: The main man, this player starts and plans robbery, also they are in charge of breaking the security.<br>
  <strong>The driver</strong>: The driver, this player gets a car and drives to and from the robbery, the getaway driver you could say, they are in charge of transport.<br>
  <strong>The weapons expert</strong>: The gunner of the team, this player has to get the guns together so the rest of the team have some force against anyone who gets in the way.<br>
  <strong>The explosives expert</strong>: The person who gets past any brick walls so you can just walk in and walk out without getting lost in a maze.  </p>

<hr align="center">
<h1 align="center"><a name="A24">Missions</a></h1>
<p align="center"><br>
The missions are the place where you are hired to perform certain jobs, in which you are rewarded for. The harder the mission, the larger the reward you receive. Always check to see if there is a new job open or you if have one still to be completed. Different Missions can be found in different locations, so fly around to see where the best sounding mission is.</p>

<hr align="center">
<h1 align="center"><a name="A25">Jail</a></h1>
<p align="center"><br>
The jail is where you end up if you get caught stealing. However, you are only in there for a certain time. Other players may also break you out of jail, and to return the favor you could try and break them out. There is also a bail option where you can pay to remove someone from jail.</p>
<p><strong>Bail Pricing:</strong> The cost for bail is $1,000 per second you have left in jail. The longer the sentence, the more expensive it is.</p>

<hr align="center">
<h1 align="center"><a name="A26">Rankbar</a></font></h3>
<p align="center"><br>
The rankbar has to be bought with Tokens. It shows how close you are to the next rank, when it gets to 100% you rank-up. But if you're at the top rank, you will not be able to rank anymore. </p>

<hr align="center">
<h1 align="center"><a name="A27">Gym</a></font></h3>
<p align="center"><br>
The Gym; lots of fights go on here. You can train, or you can fight. Your strength level is displayed, and the higher your strength and the stronger you are, and more likely you are to win the fight.</p>

<hr align="center">
<h1 align="center"><a name="A28">Networks</a></h1>
<p align="center"><br>
Networks are for players to join. If you are in a Network then you can view your Network's forums. In the Network there are 6 important people, these are:<br>
  Two leaders who own the Network and maintain it.<br>
  Left and Right hand men to look after the Network forums.<br>
  Two recruiters to recruit people into the Network<br>
Some Networks may not want to recruit certain people because of their rank or that their Network is too full. So there is a chance that you may not always get into a Network right away.<br>
Each Network is limited to having 50 members maximum.
</p>

<hr align="center">
<h1 align="center"><a name="A29">Most Wanted</a></h1>
<p align="center"><br>
The most wanted list. All the people on this list are wanted dead. Kill them, and you get the set reward. You can also put people on the list by filling out the form. Once added, people may want to kill them for the money you have set, though others may leave it and the person could stay alive.  </p>

<hr align="center">
<h1 align="center"><a name="A30">Kills</a></h1>
<p align="center"><br>
Kills, get some spies and hunt your target down. Get your gun out and unload some lead and grenades on their asses, but if you use too little bullets then you get backfire, which takes health off of you, so beware. Spies themselfs have a 25% chance of finding the person, so 1 spy=25% chance of finding, 4 spies=100% chance of finding them.  </p>

<hr align="center">
<h1 align="center"><a name="A31">Counter Spies</a></h1>
<p align="center"><br>
Have reason to believe you're getting hunted? Go to counterspies, and get some spies of your own to kill the hunters spies. And save your own life. Their spies may not all be killed, but it may help you to evade death.  </p>

<hr align="center">
<h1 align="center"><a name="A32">Airport</a></h1>
<p align="center"><br>
The airport, this is were you get to fly. You can either use your own fully repaired plane for a small fee, or, if you do not have any planes, for a larger fee you can borrow one of the airports.</p>

<hr align="center">
<h1 align="center"><a name="A49">Port</a></h1>
<p align="center"><br>
The port, this is were you get to sail. You can either use your own fully repaired vessel for a small fee, or, if you do not have any vessels, for a larger fee you can borrow one of the ports.</p>

<hr align="center">
<h1 align="center"><a name="A33">Hospital</a></h1>
<p align="center"><br>
The hospital is where you can go and heal yourself. If you shoot and didn't kill someone, or get shot at yourself, come here and get youself patched up. This makes you harder to kill than a wounded player. The healing time is 10 health for every hour you are in the Hospital. </p>

<hr align="center">
<h1 align="center"><a name="A34">Local Banking</a></font></h1>
<p align="center"><br>
The local bank. Go here to deposit your money and get some interest on it, or if you need to send someone some money, you can do it here, However, there is a fee on all transfers. The fee itself depends on who owns the bank, and in which location it resides.
  </p>

<hr align="center">
<h1 align="center"><a name="A48">Swiss Banking</a></font></h1>
<p align="center"><br>
The swiss bank is the place to safely deposit large amounts of money incase you ever get shot. It costs to create an account, but after that you can deposit endless amounts of money at you own free will. However to take it out you need to remember your account information (Number and Password) and there is an additional 6% holding fee on all money once its withdrawn.
  </p>

<hr align="center">
<h1 align="center"><a name="A35">Roulette</a></h1>
<p align="center"><br>
You can place your bet on a wide variety of numbers or colors, where you can win different amounts depending on the odds of winning. Once your bet is down, the wheel spins and you see what the outcome is and whether you win or not.</p>

