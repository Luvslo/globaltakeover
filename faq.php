<h1 align="center"><a name="A38">Russian Roulette</a></h1>
<p align="center"><br>
Russian Roulette is where you are given a gun loaded with 1 bullet, you bet, if you lose, you die, if you win, you treble your money. To play this game you need to be Contract Killer+, and you can only play once every 24 hours. </p>

